    private void rellenarTabla(){
        try{
            RegistroCalificacionesBO registroCalificacionesBO = new RegistroCalificacionesBO();
            registros = registroCalificacionesBO.buscarPorIdEstudiante(idEstudiante);
            for(RegistroCalificaciones i:registros){
                String[] datos = {i.getDocente(), i.getCurso(), i.getSalonClases()};
                this.modelo.addRow(datos);
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
