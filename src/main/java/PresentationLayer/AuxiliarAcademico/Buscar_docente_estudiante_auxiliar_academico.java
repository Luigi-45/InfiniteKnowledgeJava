    private void jtxtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtNombreKeyTyped
        if(this.getOption()!=3 && this.getOption()!=5){
            if(evt.getKeyChar()== KeyEvent.VK_ENTER){
                try{
                    vaciarTabla();

                    DocenteBO docenteBO = new DocenteBO();
                    ArrayList<Docente> docentes = docenteBO.buscar_por_nombre_completo(this.jtxtNombre.getText());

                    for(Docente i:docentes){
                        String[] datos = {i.getDni(), i.getNombreCompleto(), i.getGenero()};
                        this.modelo.addRow(datos);
                    }
                }
                catch(Exception e){
                    e.printStackTrace();
                }
            }
        }
        else{
            if(evt.getKeyChar()== KeyEvent.VK_ENTER){
                try{
                    vaciarTabla();

                    EstudianteBO estudianteBO = new EstudianteBO();
                    ArrayList<Estudiante> estudiantes = estudianteBO.buscar_por_nombre_completo(this.jtxtNombre.getText());

                    for(Estudiante i:estudiantes){
                        String[] datos = {i.getDni(), i.getNombreCompleto(), i.getGenero()};
                        this.modelo.addRow(datos);
                    }
                }
                catch(Exception e){
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jtxtNombreKeyTyped

