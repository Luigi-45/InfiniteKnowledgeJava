    private void jtblDocenteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblDocenteMouseClicked
        switch(this.getOption()){
            case 1:
                try{
                    DocenteBO docenteBO = new DocenteBO();
                    Docente docente = docenteBO.buscarPorDNI((String) this.modelo.getValueAt(this.jtblDocente.getSelectedRow(), 0));
                    Asignar_cursos_auxiliar_academico ventana = new Asignar_cursos_auxiliar_academico();
                    ventana.setDocente(docente);
                    ventana.setDni(dni);
                    ventana.setNombreCompleto(nombreCompleto);
                    ventana.setCorreoElectronico(correoElectronico);
                    ventana.setVisible(true);
                    this.dispose();
                }
                catch(Exception e){
                    e.printStackTrace();
                }
                break;
            case 2:
                try{
                    DocenteBO docenteBO = new DocenteBO();
                    UsuarioBO usuarioBO = new UsuarioBO();
                    Docente docente = docenteBO.buscarPorDNI((String) this.modelo.getValueAt(this.jtblDocente.getSelectedRow(), 0));
                    Gestionar_docente_estudiantes ventana = new Gestionar_docente_estudiantes();
                    ventana.setDocente(docente);
                    ventana.setDni(dni);
                    ventana.setNombreCompleto(nombreCompleto);
                    ventana.setCorreoElectronico(correoElectronico);
                    ventana.setUsuario(usuarioBO.buscarPorDni((String) this.modelo.getValueAt(this.jtblDocente.getSelectedRow(), 0)));
                    ventana.setVisible(true);
                    this.dispose();
                }
                catch(Exception e){
                    e.printStackTrace();
                }
                break;
            case 3:
                try{
                    EstudianteBO estudianteBO = new EstudianteBO();
                    UsuarioBO usuarioBO = new UsuarioBO();
                    Estudiante estudiante = estudianteBO.buscar_por_dni((String) this.modelo.getValueAt(this.jtblDocente.getSelectedRow(), 0));
                    Gestionar_docente_estudiantes ventana = new Gestionar_docente_estudiantes();
                    ventana.setEstudiante(estudiante);
                    ventana.setDni(dni);
                    ventana.setNombreCompleto(nombreCompleto);
                    ventana.setCorreoElectronico(correoElectronico);
                    ventana.setUsuario(usuarioBO.buscarPorDni((String) this.modelo.getValueAt(this.jtblDocente.getSelectedRow(), 0)));
                    ventana.setVisible(true);
                    this.dispose();
                }
                catch(Exception e){
                    e.printStackTrace();
                }
                break;
            case 4:
                try{
                    DocenteBO docenteBO = new DocenteBO();
                    Docente docente = docenteBO.buscarPorDNI((String) this.modelo.getValueAt(this.jtblDocente.getSelectedRow(), 0));
                    Asignar_estudiante_auxiliar_academico ventana = new Asignar_estudiante_auxiliar_academico();
                    ventana.setDocente(docente);
                    ventana.setDni(dni);
                    ventana.setNombreCompleto(nombreCompleto);
                    ventana.setCorreoElectronico(correoElectronico);
                    if(this.getEstudiante()!=null){
                        ventana.setEstudiante(estudiante);
                    }
                    ventana.setVisible(true);
                    this.dispose();
                }
                catch(Exception e){
                    e.printStackTrace();
                }
                break;
            case 5:
                try{
                    EstudianteBO estudianteBO = new EstudianteBO();
                    Estudiante estudiante = estudianteBO.buscar_por_dni((String) this.modelo.getValueAt(this.jtblDocente.getSelectedRow(), 0));
                    Asignar_estudiante_auxiliar_academico ventana = new Asignar_estudiante_auxiliar_academico();
                    ventana.setEstudiante(estudiante);
                    ventana.setDni(dni);
                    ventana.setNombreCompleto(nombreCompleto);
                    ventana.setCorreoElectronico(correoElectronico);
                    if(this.getDocente()!=null){
                        ventana.setDocente(docente);
                    }
                    ventana.setVisible(true);
                    this.dispose();
                }
                catch(Exception e){
                    e.printStackTrace();
                }
                break;
                
        }
    }//GEN-LAST:event_jtblDocenteMouseClicked

