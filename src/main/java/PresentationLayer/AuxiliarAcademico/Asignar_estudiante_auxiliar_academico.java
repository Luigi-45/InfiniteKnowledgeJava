    private void jtxtCursoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtCursoKeyTyped
        if(evt.getKeyChar()== KeyEvent.VK_ENTER){
            try{
                vaciarTabla();

                CursoBO cursoBO = new CursoBO();
                ArrayList<Curso> cursos = cursoBO.buscarPorNombre(this.jtxtCurso.getText());

                for(Curso i:cursos){
                    String[] datos = {i.getNombre()};
                    this.modelo.addRow(datos);
                }
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jtxtCursoKeyTyped

