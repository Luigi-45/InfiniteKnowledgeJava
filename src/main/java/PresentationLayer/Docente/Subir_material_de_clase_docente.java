    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        DbxRequestConfig config = DbxRequestConfig.newBuilder("dropbox/java-tutorial").build();
        DbxClientV2 client = new DbxClientV2(config, ACCESS_TOKEN);
        
        try{
            File archivoASubir = new File(this.ruta);
            InputStream inputStream = new FileInputStream(archivoASubir);
            
            UploadBuilder uploadBuilder = client.files().uploadBuilder("/"+(String) this.listaCursos.getSelectedItem()+"/"+archivoASubir.getName());
            uploadBuilder.withClientModified(new Date(archivoASubir.lastModified()));
            uploadBuilder.withMode(WriteMode.ADD);
            uploadBuilder.withAutorename(true);
            
            uploadBuilder.uploadAndFinish(inputStream);
            
            inputStream.close();
            
            JOptionPane.showMessageDialog(null, "El material de clase se subió satisfactoriamente");
        }
        catch(DbxException e){
            e.printStackTrace();
        }
        catch(FileNotFoundException e){
            e.printStackTrace();
        }       
        catch (IOException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton9ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        JFileChooser selector = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Material de clase","pdf");
        selector.setFileFilter(filtro);
        selector.setName("Guardar material de clase");
        selector.setAcceptAllFileFilterUsed(false);
        if(selector.showSaveDialog(null)== JFileChooser.APPROVE_OPTION){
            this.ruta = selector.getSelectedFile().toString();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

