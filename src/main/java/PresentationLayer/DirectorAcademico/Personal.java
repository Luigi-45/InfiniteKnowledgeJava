    private void actualizarDatosAuxiliarAcademico(){
        this.jtxtDNI.setText(this.auxiliarAcademico.getDni());
        this.jtxtNombre.setText(this.auxiliarAcademico.getNombre());
        this.jtxtApellidoPaterno.setText(this.auxiliarAcademico.getApellidoPaterno());
        this.jtxtApellidoMaterno.setText(this.auxiliarAcademico.getApellidoMaterno());
        switch(this.getAuxiliarAcademico().getGenero()){
            case "Hombre":
                this.jcoGenero.setSelectedIndex(0);
                break;
            case "Mujer":
                this.jcoGenero.setSelectedIndex(1);
                break;
        }
        this.jcaFechaNacimiento.setDate(this.auxiliarAcademico.getFechaNacimiento());
        this.jtxtNumeroTelefonico.setText(this.auxiliarAcademico.getNumeroTelefonico());
        switch(this.auxiliarAcademico.getGradoAcademico()){
            case "Licenciado":
                this.jcoGradoAcademico.setSelectedIndex(0);
                break;
            case "Magister":
                this.jcoGradoAcademico.setSelectedIndex(1);
                break;
            case "Doctor":
                this.jcoGradoAcademico.setSelectedIndex(2);
                break;
        }
        this.jtxtCorreoE.setText(this.getUsuario().getCorreoElectronico());
    }
    
