    private void establecerValores(){
        this.jtxtNombre.setText(this.getCurso().getNombre());
        this.jtxtNumHoras.setText(String.valueOf(this.getCurso().getnHoras()));
        switch(this.getCurso().getEnfoqueCurso()){
            case "Ciencias":
                this.jcoEnfoque.setSelectedIndex(0);
                break;
            case "Humanidades":
                this.jcoEnfoque.setSelectedIndex(1);
                break;    
        }
    }
    
    private void vaciar(){
        this.jtxtNombre.setText(null);
        this.jtxtNumHoras.setText(null);
    }
    
