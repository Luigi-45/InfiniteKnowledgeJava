    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try{
            if(this.jtxtNombre.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Error, ingrese el nombre del curso");
            }
            else if(this.jtxtNumHoras.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Error, ingrese el número de horas");
            }
            else{
                CursoBO cursoBO = new CursoBO();
                
                Curso curso = new Curso(0,this.jtxtNombre.getText(),this.jtxtNumHoras.getText(),(String)this.jcoEnfoque.getSelectedItem());
                
                cursoBO.insertar(curso);
                
                JOptionPane.showMessageDialog(null, "Se ha logrado registrar el curso correctamente");
            }
        }
        catch(Exception e){
            if(e.getMessage().contains("idx_curso_nombre")){
                JOptionPane.showMessageDialog(null, "El nombre del curso ingresado ya existe en la base de datos");
            }
            
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jtxtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtNombreKeyTyped
        char caracter = evt.getKeyChar(); 
        if(!((caracter<'0')||(caracter>'9')) && ((caracter!='\b') || (caracter!='\t'))){
            evt.consume();
        }
        if(this.jtxtNombre.getText().length()==45) evt.consume();
    }//GEN-LAST:event_jtxtNombreKeyTyped

    private void jtxtNumHorasKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtNumHorasKeyTyped
        char caracter = evt.getKeyChar(); 
        if(((caracter<'0')||(caracter>'9')) && ((caracter!='\b') || (caracter!='\t'))){
            evt.consume();
        }
        if(this.jtxtNumHoras.getText().length()==2) evt.consume();
    }//GEN-LAST:event_jtxtNumHorasKeyTyped

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try{
            if(this.jtxtNombre.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Error, ingrese el nombre del curso");
            }
            else if(this.jtxtNumHoras.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Error, ingrese el número de horas");
            }
            else{
                CursoBO cursoBO = new CursoBO();
                
                Curso curso = new Curso(this.getCurso().getCursoId(),this.jtxtNombre.getText(),this.jtxtNumHoras.getText(),(String)this.jcoEnfoque.getSelectedItem());
                
                cursoBO.actualizar(curso);
                
                JOptionPane.showMessageDialog(null, "Se ha logrado actualizar el curso correctamente");
                
                this.setCurso(curso);
            }
        }
        catch(Exception e){
            if(e.getMessage().contains("idx_curso_nombre")){
                JOptionPane.showMessageDialog(null, "El nombre del curso ingresado ya existe en la base de datos");
            }       
            
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

