    private void jtblMiembroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblMiembroMouseClicked
        switch(this.getRol()){
            case 0:
                try{
                    DirectorAcademicoBO directorAcademicoBO = new DirectorAcademicoBO();
                    UsuarioBO usuarioBO = new UsuarioBO();
                    Personal ventana = new Personal();
                    ventana.setNombreCompleto(this.getNombreCompleto());
                    ventana.setCorreoElectronico(this.getCorreoElectronico());
                    ventana.setDirectorAcademico(directorAcademicoBO.buscarPorId());
                    ventana.setUsuario(usuarioBO.buscarPorDni(directorAcademicoBO.buscarPorId().getDni()));
                    ventana.setRolSeleccionado(0);
                    ventana.setVisible(true);
                    this.dispose();
                }
                catch(Exception e){
                    e.printStackTrace();
                }
                break;
            case 1:
                try{
                    AuxiliarAcademicoBO auxiliarAcademicoBO = new AuxiliarAcademicoBO();
                    UsuarioBO usuarioBO = new UsuarioBO();
                    Personal ventana = new Personal();
                    ventana.setNombreCompleto(this.getNombreCompleto());
                    ventana.setCorreoElectronico(this.getCorreoElectronico());
                    ventana.setAuxiliarAcademico(auxiliarAcademicoBO.buscarPorDNI((String) this.modelo.getValueAt(this.jtblMiembro.getSelectedRow(), 0)));
                    ventana.setUsuario(usuarioBO.buscarPorDni((String) this.modelo.getValueAt(this.jtblMiembro.getSelectedRow(), 0)));
                    ventana.setRolSeleccionado(1);
                    ventana.setVisible(true);
                    this.dispose();
                }
                catch(Exception e){
                    e.printStackTrace();
                }
                break;
        }
    }//GEN-LAST:event_jtblMiembroMouseClicked

    private void jtxtNombreCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtNombreCKeyTyped
        if(evt.getKeyChar()== KeyEvent.VK_ENTER){   
            try{
                AuxiliarAcademicoBO auxiliarAcademicoBO = new AuxiliarAcademicoBO();
                ArrayList<AuxiliarAcademico> auxiliaresAcademicos = auxiliarAcademicoBO.buscarPorNombreCompleto(this.jtxtNombreC.getText());
                vaciarTabla();
                    
                for(AuxiliarAcademico i:auxiliaresAcademicos){
                    String[] datos = {i.getDni(), i.getNombreCompleto(), i.getGenero()};
                    this.modelo.addRow(datos);
                }
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jtxtNombreCKeyTyped

