    public void setRol(int rol) {
        this.rol = rol;
        switch(this.getRol()){
            case 0:
                this.jlBuscarMiembro.setText("Buscar Director A.");
                this.jlNombreC.setVisible(false);
                this.jtxtNombreC.setVisible(false);
                try{
                    DirectorAcademicoBO directorAcademicoBO = new DirectorAcademicoBO();
                    DirectorAcademico directorAcademico = directorAcademicoBO.buscarPorId();
                    String[] datos = {directorAcademico.getDni(), directorAcademico.getNombreCompleto(), directorAcademico.getGenero()};
                    vaciarTabla();
                    this.modelo.addRow(datos);
                }
                catch(Exception e){
                    e.printStackTrace();
                }
                break;
            case 1:
                this.jlBuscarMiembro.setText("Buscar Auxiliar A.");
                this.jlNombreC.setVisible(true);
                this.jtxtNombreC.setVisible(true);
                break;
        }
    }
    
    private void vaciarTabla(){
        int fila = this.jtblMiembro.getRowCount();
        for(int i=fila-1; i>=0; i--){
            this.modelo.removeRow(i);
        }
    }
