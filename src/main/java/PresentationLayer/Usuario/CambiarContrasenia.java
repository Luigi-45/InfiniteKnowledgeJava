    public void setNombreCompleto(String nombreCompleto) {
        this.nombreCompleto = nombreCompleto;
        this.jlNombre.setText(this.nombreCompleto);
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        try{
            this.dni = dni;
            UsuarioBO usuarioBO = new UsuarioBO();
            this.usuario = usuarioBO.buscarPorDni(this.getDni());
            this.jtxtCorreoElectronico.setText(usuario.getCorreoElectronico());
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }

