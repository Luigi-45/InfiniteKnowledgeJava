    private void jtCursoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtCursoMouseClicked
        try{
            if(Desktop.isDesktopSupported()){
                Desktop escritorio = Desktop.getDesktop();
                if(escritorio.isSupported(Desktop.Action.BROWSE)){
                    escritorio.browse(new URI("https://www.dropbox.com/home/"+ String.valueOf(this.modelo.getValueAt(this.jtCurso.getSelectedRow(), 0)).replace(" ", "%20")));
                }
            }
            CrearQR crear = new CrearQR();
            BufferedImage imagen = crear.crearQR("https://www.dropbox.com/home/"+ String.valueOf(this.modelo.getValueAt(this.jtCurso.getSelectedRow(), 0)).replace(" ", "%20"), 300, 300);
            ImageIcon icono = new ImageIcon(imagen);
            this.jlCodigoQR.setIcon(icono);
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jtCursoMouseClicked

